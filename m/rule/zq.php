			&nbsp;</li>
		<li>
			如果赛事在上半场时节因任何理由取消或中断，所有上半场注单将被取消。除非在赛事取消或中断前，结果已经明确。<br />
			&nbsp;</li>
		<li>
			如果赛事在下半场或加时时段因任何理由取消或中断，所有上半场注单保持有效。</li>
	</ol>
	<p>
		<strong>&nbsp;滚球大小盘</strong></p>
	<ol>
		<li>
			<p>
				结算是以比赛/时节结束后的总入球数做裁决。投注当时，赛事的比分将视为0-0来计算。</p>
		</li>
	</ol>
	<p>
		<strong>加时赛-大小盘</strong></p>
	<ol>
		<li>
			<p>
				两支球队开始加时赛的比分为0-0，之前赛果属于常规时间内入球不会计算在内。</p>
		</li>
		<li>
			<p>
				所有的投注将以30分钟加时赛后结果结算，包括补时。</p>
		</li>
		<li>
			在加时赛结束前如果比赛停止，取消或中断，所有投注将被视为无效，除非在赛事取消或中断前，结果已经明确。</li>
	</ol>
	<p>
		<strong>半场加时赛-大小盘</strong></p>
	<ol type="1">
		<li>
			所有投注将会按照15分钟赛事结束后赛果为准，包含补时。<br />
			&nbsp;</li>
		<li>
			如果比赛在上半场停止，取消或者中断，所有上半场注单将被视为无效。<br />
			&nbsp;</li>
		<li>
			如果比赛在下半场或补时期间停止，取消或者中断，所有上半场注单将被视为有效。</li>
	</ol>
	<p>
		<strong>单一球队大小盘</strong></p>
	<ol>
		<li>
			<p>
				预测在特定的比赛有关期间内，其中一支球队的总入球数将大于或小于在盘口指定的大/小盘球数。</p>
		</li>
		<li>
			<p>
				如果赛事的总入球数多于盘口的大/小盘球数，则为大盘。如果少于盘口的大/小盘球数，则为小盘。</p>
