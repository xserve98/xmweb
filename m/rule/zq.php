	<p>
		<strong>大小盘</strong></p>
	<br />
	<p>
		<strong>一般规则</strong></p>
	<ol>
		<li>
			预测赛事总入球数将大于或小于在盘口指定的大/小盘球数。<br />
			&nbsp;</li>
		<li>
			如果赛事的总入球数多于盘口的大/小盘球数，则为大盘。如果少于盘口的大/小盘球数，则为小盘。<br />
			&nbsp;</li>
		<li>
			所有注单将按盘口开出的大/小盘球数在玩法的时节结束后结算。<br />
			&nbsp;</li>
		<li>
			大/小盘的玩法分为以下几种：
			<ol type="a">
				<li>
					大/小于&#39;一球&#39;（如：2，3，4，等）<br />
					&nbsp;</li>
				<li>
					大/小于&#39;半球&#39;（如：1.5，2.5，3.5，等）<br />
					&nbsp;</li>
				<li>
					混合以上&#39;半球/一球&#39;的方式（如：1.5/2，2.5/3，3.5/4，等）</li>
			</ol>
		</li>
	</ol>
	<ol start="5">
		<li>
			<p>
				如果赛事中断前已有明确结果并且之后没有任何显著会影响赛事结果的情况，大/小盘注单才会被结算。若遇到任何其他情况，注单将一律被取消。请参考以上范例：<br />
				&nbsp;</p>
			<ol type="a">
				<li>
					范例1：会员投注大于2.5球：
					<ol type="i">
						<li>
							赛事在比分2-1时中断。<br />
							&nbsp;</li>
						<li>
							尽管赛事中断，但因结果已经明确并且若之后有任何潜在进球将对盘口结算裁决没有影响，所有此会员注单结算为赢。<br />
							&nbsp;</li>
					</ol>
				</li>
				<li>
					范例2：会员投注小于2.5球：
					<ol type="i">
						<li>
							<p>
								赛事在比分2-1时中断。<br />
								&nbsp;</p>
						</li>
						<li>
							尽管赛事中断，但因结果已经明确并且若之后有任何潜在进球将对盘口结算裁决没有影响，所有此会员注单结算为输。<br />
							&nbsp;</li>
					</ol>
				</li>
				<li>
					范例3：会员投注大于3.5球：
					<ol type="i">
						<li>
							赛事在比分2-1时中断。<br />
							&nbsp;</li>
						<li>
							由于赛事在未有明确的结果能裁决会员的注单前中断，此会员的注单将被取消。</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>
