    <h5>◆足球规则</h5>
<div>
<p class="b sub">1. 一般规则</p>
    <ul style="list-style:decimal;">
		<li>
			除非另有注明，所有足球投注的结算皆以球赛所规定的完场时间90分钟为准。<br />
			&nbsp;</li>
		<li>
			完场时间90分钟包括球员伤停补时。加时赛、淘汰赛、点球，以及赛事后如果裁判或相关管理机构更改任何赛果则不计算在内。<br />
			&nbsp;</li>
		<li>
			除非在个别玩法规则另有注明，所有滚球投注的结算将以90分钟的赛果为准。<br />
			&nbsp;</li>
		<li>
			对于某些以全场完场时间为80分钟（2 x40分钟）的特定赛事或者友谊赛，所有投注的结算皆以完场时间为准。<br />
			&nbsp;</li>
		<li>
			若少年赛、友谊赛的完场时间为70分钟（2 x 35分钟）或更少，本公司将在开赛前做出公布，否则该场赛事的注单一律作废。<br />
			&nbsp;</li>
		<li>
			如果赛事中断或延迟，并且没有在36小时内重新开始，所有该场赛事的投注即被视为无效且取消，除非在个别投注类型规则里另有指定注明。<br />
			&nbsp;</li>
		<li>
			如果赛事确认取消，所有该场赛事的投注即被视为无效且取消，除非在个别投注类型规则里另有指定注明。如果赛事是在上半场中断，所有上半场的注单将被取消。如果赛事是在下半场中断所有上半场的投注保持有效，但所有下半场的注单将被取消，除非在个别玩法规则另有注明。<br />
			&nbsp;</li>
		<li>
			除非在个别玩法规则另有注明，乌龙球将予以计算在内。<br />
			&nbsp;</li>
		<li>
			如果比赛场地有变更（主客队调换），所有此注单将被取消。<br />
			&nbsp;</li>
		<li>
			对于国际赛事，只要变更的场地仍在同一个国家内，所有注单将保持有效。<br />
			&nbsp;</li>
		<li>
			对于国际赛事，只要变更的场地仍在联赛原定举办的国家内，所有注单将保持有效。<br />
			&nbsp;</li>
		<li>
			本公司保留权利取消任何因更换场地而可能对赛事结果有影响的注单。</li>
	</ol>
