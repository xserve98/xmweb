	<p>
		<strong>让球盘（亚洲让球盘）</strong><br />
		<br />
		<strong>一般规则</strong></p>
	<ol>
		<li>
			预测哪一支球队在盘口指定的让球数在全场/半场/赛事某个时节赢得比赛。<br />
			&nbsp;</li>
		<li>
			&ldquo;让球盘&rdquo;则定义为在比赛正式开始前，一方球队已得到另一方让的虚拟分数，以领先的情况下开始比赛。<br />
			&nbsp;</li>
		<li>
			所有注单将按盘口开出的让球数在玩法的时节结束后结算。<br />
			&nbsp;</li>
		<li>
			让球队（优势球队）将给出让球，让球数将显示在赔率的左侧，让球队在盘面上也会以粗型字体显示<br />
			&nbsp;</li>
		<li>
			让球盘的玩法分为以下几种：<br />
			<br />
			a.整数让球也或称为让&lsquo;一球&rsquo;（如：-1，-2，-3，等）<br />
			<br />
			b.非整数让球也或称为&lsquo;半球&rsquo;（如：-0.5，-1.5，-2.5，等）<br />
			<br />
			c.混合以上让&lsquo;半球/一球&rsquo;的方式（如：-0/0.5，-0.5/1，-1/1.5，等）<br />
			&nbsp;</li>
	</ol>
	<p>
		<strong>全场让球盘（亚洲让球盘）</strong></p>
	<ol>
		<li>
			根据盘口让球信息预测最终获得胜利的球队。<br />
			&nbsp;</li>
		<li>
			投注的结算皆以球赛所规定的完场时间90分钟为准。<br />
			&nbsp;</li>
		<li>
			如果赛事在90分钟结束前取消或中断，所有注单将会被视为无效。</li>
	</ol>
	<p>
		<strong>半场让球盘（亚洲让球盘）</strong></p>
	<ol>
		<li>
			所有上半场的投注将以上半场45分钟其中包含伤停补时后的赛果结算。<br />
			&nbsp;</li>
		<li>
			如果赛事在上半场时节因任何理由取消或中断，所有上半场注单将被取消。<br />
			&nbsp;</li>
		<li>
			如果赛事在下半场或加时赛因任何理由取消或中断，所有上半场注单保持有效。</li>
	</ol>
	<p>
		<strong>滚球让球盘</strong></p>
	<ol>
		<li>
			<p>
				所有注单将按照盘口开出让球信息，在相应投注类型结束后结算。</p>
		</li>
		<br />
		<li>
			<p>
				结算是以投注时到比赛/时节结束后的赛果做裁决。即是以赛事完场比分减去投注当时的比分。上半场的滚球让球投注将以上半场结束后的赛果结算。&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
		</li>
	</ol>
	<strong>全场补时（亚洲让球盘）</strong><br />
	<ol>
		<li>
			所有注单将按照盘口开出让球信息，在30分钟加时赛结束后计算，包含补时。<br />
			&nbsp;</li>
		<li>
			如果赛事在加时赛结束前取消或中断，所有注单将会被视为无效。</li>
	</ol>
	<p>
		<strong>半场补时（亚洲让球盘）</strong></p>
	<ol>
		<li>
			所有注单将按照盘口开出让球信息，在15分钟加时赛结束后计算，包含补时。<br />
			&nbsp;</li>
		<li>
			加时赛中如果赛事在上半场取消或中断，所有上半场注单将会被视为无效。<br />
			&nbsp;</li>
		<li>
			加时赛中如果赛事在下半场或补时阶段取消或中断，所有上半场注单将会被视为有效。</li>
	</ol>
	<p>
		<strong>15</strong><strong>分钟比赛（亚洲让球盘）</strong></p>
	<ol>
		<li>
